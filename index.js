

// NAV MENU

let menu_items = document.getElementById("menu-items");

menu_items.style.maxHeight = "0px";

function MenuToggle(){
    if
    (menu_items.style.maxHeight == "0px")
    {
    
        menu_items.style.maxHeight = "700px";
       

    }
    else
    {menu_items.style.maxHeight = "0px"}
        
    
}